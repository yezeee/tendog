package com.fin.prj.dto;

public class DssDTO
{
	String dss_cd, part_cd, dss_name, dss_symptoms;

	public String getDss_cd()
	{
		return dss_cd;
	}

	public void setDss_cd(String dss_cd)
	{
		this.dss_cd = dss_cd;
	}

	public String getPart_cd()
	{
		return part_cd;
	}

	public void setPart_cd(String part_cd)
	{
		this.part_cd = part_cd;
	}

	public String getDss_name()
	{
		return dss_name;
	}

	public void setDss_name(String dss_name)
	{
		this.dss_name = dss_name;
	}

	public String getDss_symptoms()
	{
		return dss_symptoms;
	}

	public void setDss_symptoms(String dss_symptoms)
	{
		this.dss_symptoms = dss_symptoms;
	}
	
	
}
